#include "libft.h"

int	ft_strnequ(char const *s1, char const *s2, size_t n)
{
	int i;

	i = 0;
	if ((s1 == NULL) || (s2 == NULL))
		return (0);
	while ((s1[i] == s2[i]) && (n > 0))
	{
		if ((s1[i] == '\0') && (n - 1 == 0))
			return (1);
		i++;
		n--;
	}
	if (n == 0)
		return (1);
	return (0);
}
